#!/usr/bin/env node
/**
 * 将 public/ 下的静态文件内联到 assets-content.js，供 Worker 直接返回，不依赖 [assets] 绑定。
 * 部署前需运行：node build-embed-assets.js
 */
import { readFileSync, writeFileSync } from 'fs';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const publicDir = join(__dirname, 'public');

const files = [
  ['index.html', 'text/html; charset=utf-8'],
  ['style.css', 'text/css; charset=utf-8'],
  ['app.js', 'application/javascript; charset=utf-8'],
];

const ASSETS_CONTENT = {};
const ASSETS_TYPES = {};

for (const [name, contentType] of files) {
  const path = join(publicDir, name);
  const content = readFileSync(path, 'utf-8');
  ASSETS_CONTENT['/' + name] = content;
  ASSETS_TYPES['/' + name] = contentType;
}
ASSETS_TYPES['/'] = ASSETS_TYPES['/index.html'];

const outPath = join(__dirname, 'assets-content.js');
const out = `// Generated by build-embed-assets.js - do not edit manually
export const ASSETS_CONTENT = ${JSON.stringify(ASSETS_CONTENT)};
export const ASSETS_TYPES = ${JSON.stringify(ASSETS_TYPES)};
`;
writeFileSync(outPath, out, 'utf-8');
console.log('Written', outPath);
